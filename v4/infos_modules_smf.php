	<strong id="2">2. </strong> <u>envoiImages</u> : un formulaire d'envoi d'images
	<br />
	<p class="infos">
		Ce module ajoute un formulaire d'envoi d'images pour les pages de rédaction des messages.
		Le formulaire est situé en dessous du textarea principal.
		Les opérations sont indépendantes du reste de la page puisque le formulaire est construit autour d'une formule <acronym title='Asynchronous JavaScript and XML'>AJAX</acronym>.
		<br />
		<br />
		Module disponible depuis le <span class="dispo">25 Juillet 2008</span>.
	</p>


