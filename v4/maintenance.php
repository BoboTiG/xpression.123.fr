	<p class="p_titre">Utilitaires de maintenance</p>
	
	<p>
		Lors de mes stages et diverses opérations chez des clients/connaissances/amis, j'avais toujours besoin de quelques programmes précis.
		<br />
		Que ce soit pour nettoyer le poste des spywares et virus, des fichiers temporaires ou encore faire certaines vérifications.
		<br />
		<br />
		C'est là qu'est né, un 19 Janvier 2007, <strong>Maintenance</strong>.
		<br />
		C'est un script batch qui, associé à <a href="http://home.mnet-online.de/horst.muc/ui.htm" title="Site de l'auteur" onclick='window.open(this.href);return(false);'>WBAT</a>, permet d'avoir un menu simple et efficace pour lancer ses applications.
		<br />
		<br />
		Tous les programmes sont <em>portables</em>, ils ne laissent pas de traces sur l'ordinateur hôte.
	</p>

