	<p class="p_titre">Script : Xara Xtreme pour Debian Etch</p>
	
	<p>
		Il s'agit d'un <strong>script</strong> qui permet de :
		<ul>
			<li>télécharger Xara Xtreme v0.7 et ses dépendances</li>
			<li>supprimer Xara Xtreme v0.7 installé à l'aide de ce script</li>
		</ul>
	</p>
	
	<p>
		Il suffit de lancer le script tel que :
		<blockquote>./xaralx.sh</blockquote>
		ou encore :
		<blockquote>sh xaralx.sh</blockquote>
	</p>
	
